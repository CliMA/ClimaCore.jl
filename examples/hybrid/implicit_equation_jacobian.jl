struct ImplicitEquationJacobian{TJ, RJ, F, T1, T2}
    # nonzero blocks of the implicit tendency's Jacobian
    вҲӮYвӮңвҲӮY::TJ

    # the full implicit residual's Jacobian, and its linear solver
    вҲӮRвҲӮY::RJ

    # whether this struct is used to compute Wfact_t or Wfact
    transform::Bool

    # flags for computing the Jacobian
    flags::F

    # cache that is used to evaluate ldiv!
    temp1::T1
    temp2::T2
end

function ImplicitEquationJacobian(Y, transform, flags)
    FT = eltype(Y)

    б¶ңПҒ_name = @name(c.ПҒ)
    б¶ңрқ”ј_name = if :ПҒОё in propertynames(Y.c)
        @name(c.ПҒОё)
    elseif :ПҒe in propertynames(Y.c)
        @name(c.ПҒe)
    elseif :ПҒe_int in propertynames(Y.c)
        @name(c.ПҒe_int)
    end
    б¶ рқ•„_name = @name(f.w)

    BidiagonalRow_C3 = BidiagonalMatrixRow{C3{FT}}
    BidiagonalRow_ACT3 = BidiagonalMatrixRow{Adjoint{FT, CT3{FT}}}
    QuaddiagonalRow_ACT3 = QuaddiagonalMatrixRow{Adjoint{FT, CT3{FT}}}
    TridiagonalRow_C3xACT3 =
        TridiagonalMatrixRow{typeof(C3(FT(0)) * CT3(FT(0))')}
    вҲӮб¶ңрқ”јвӮңвҲӮб¶ рқ•„_Row_ACT3 =
        flags.вҲӮб¶ңрқ”јвӮңвҲӮб¶ рқ•„_mode == :exact && :ПҒe in propertynames(Y.c) ?
        QuaddiagonalRow_ACT3 : BidiagonalRow_ACT3
    вҲӮYвӮңвҲӮY = FieldMatrix(
        (б¶ңПҒ_name, б¶ рқ•„_name) => Fields.Field(BidiagonalRow_ACT3, axes(Y.c)),
        (б¶ңрқ”ј_name, б¶ рқ•„_name) => Fields.Field(вҲӮб¶ңрқ”јвӮңвҲӮб¶ рқ•„_Row_ACT3, axes(Y.c)),
        (б¶ рқ•„_name, б¶ңПҒ_name) => Fields.Field(BidiagonalRow_C3, axes(Y.f)),
        (б¶ рқ•„_name, б¶ңрқ”ј_name) => Fields.Field(BidiagonalRow_C3, axes(Y.f)),
        (б¶ рқ•„_name, б¶ рқ•„_name) =>
            Fields.Field(TridiagonalRow_C3xACT3, axes(Y.f)),
    )

    dtОі = FT(1)
    I = MatrixFields.identity_field_matrix(Y) # one(вҲӮYвӮңвҲӮY) can't get every block
    вҲӮRвҲӮY = transform ? I ./ dtОі .- вҲӮYвӮңвҲӮY : dtОі .* вҲӮYвӮңвҲӮY .- I
    alg = MatrixFields.BlockArrowheadSolve(б¶ңПҒ_name, б¶ңрқ”ј_name)

    return ImplicitEquationJacobian(
        вҲӮYвӮңвҲӮY,
        FieldMatrixWithSolver(вҲӮRвҲӮY, Y, alg),
        transform,
        flags,
        similar(Y),
        similar(Y),
    )
end

# Required for compatibility with OrdinaryDiffEq.jl
Base.similar(A::ImplicitEquationJacobian) = ImplicitEquationJacobian(
    similar(A.вҲӮYвӮңвҲӮY),
    similar(A.вҲӮRвҲӮY),
    A.transform,
    A.flags,
    A.temp1,
    A.temp2,
)

# Required for compatibility with ClimaTimeSteppers.jl
